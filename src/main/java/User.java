public class User {
    
    private String name;
    private int baseNumber;
    
    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setBaseNumber(int baseNumber) {
        this.baseNumber = baseNumber;
    }

    public int getBaseNumber() {
        return baseNumber;
    }
}
